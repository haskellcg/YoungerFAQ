package youngerFAQ.commons.sub;

public class AnswerMes {
	
	
	private String ansId;				//答案ID
	private String ansToProId;			//与答案匹配的问题ID
	private String ansContent;			//答案内容
	private String ansPersonname;		//回答人
	
	
	
	
	public String getAnsId() {
		return ansId;
	}
	public void setAnsId(String ansId) {
		this.ansId = ansId;
	}
	public String getAnsToProId() {
		return ansToProId;
	}
	public void setAnsToProId(String ansToProId) {
		this.ansToProId = ansToProId;
	}
	public String getAnsContent() {
		return ansContent;
	}
	public void setAnsContent(String ansContent) {
		this.ansContent = ansContent;
	}
	public String getAnsPersonname() {
		return ansPersonname;
	}
	public void setAnsPersonname(String ansPersonname) {
		this.ansPersonname = ansPersonname;
	}
	
	
	
	

}
